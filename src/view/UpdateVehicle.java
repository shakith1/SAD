                String qry = "UPDATE delivery_vehicle SET delivery_vehicle.make ='" + jComboBox1.getSelectedItem().toString() + "' , delivery_vehicle.category = '" + jComboBox2.getSelectedItem().toString() + "' , delivery_vehicle.fuelType='" + jComboBox3.getSelectedItem().toString() + "' , delivery_vehicle.capacity = '" + jTextField3.getText() + "' , delivery_vehicle.description='" + jTextArea1.getText() + "' , delivery_vehicle.path ='" + path + "' WHERE vehicleID = '" + jTextField1.getText() + "'";
